import Foundation
import UIKit

func FONT_LIGHT(_ size:CGFloat) -> UIFont? {
    return UIFont(name: "IRANSans(FaNum) Light", size: size)
}

func FONT_MEDIUM(_ size:CGFloat) -> UIFont? {
    return UIFont(name: "IRANSansFaNum-Medium", size: size)
}

func FONT_NORMAL(_ size:CGFloat) -> UIFont? {
    return UIFont(name: "IRANSans(FaNum)", size: size)
}

func FONT_BOLD(_ size:CGFloat) -> UIFont? {
    return UIFont(name: "IRANSansMobile-Bold", size: size)
}
